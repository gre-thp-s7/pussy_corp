  # User create + Cart create
  def create
    @user = User.new(user_params)

    respond_to do |format|
      if @user.save
        @user.create_cart!
        format.html { redirect_to root_path, notice: 'User was successfully created.' }
      else
        format.html { render :new }
      end
    end
  end


